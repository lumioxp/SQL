DECLARE @i INT = 1;
DECLARE @SupplierID INT;
DECLARE @Cost DECIMAL(10, 2);
DECLARE @Quantity INT;

WHILE @i <= 200
BEGIN
    -- Pobranie SupplierID dla bie¿¹cej iteracji, cyklicznie wybierane spoœród 8 SupplierID
    SET @SupplierID = (SELECT TOP 1 sup.SupplierID
                       FROM (SELECT ROW_NUMBER() OVER (ORDER BY SupplierID) AS RowNum, SupplierID
                       FROM SupplierCategory) AS sup
                       WHERE sup.RowNum = ((@i - 1) % 8) + 1);

    -- Pobranie wartoœci Cost dla ProductID = @i
    SET @Cost = (SELECT TOP 1 (p.Price * i.Quantity) AS TotalValue
                 FROM Product p
                 JOIN Inventory i ON p.ProductID = i.ProductID
                 WHERE i.ProductID = @i);

    -- Pobranie wartoœci Quantity dla ProductID = @i
    SET @Quantity = (SELECT TOP 1 Quantity 
                     FROM Inventory 
                     WHERE ProductID = @i);

    -- Wstawianie wiersza do tabeli ProductSupplier
    INSERT INTO ProductSupplier (ProductID, SupplierID, SupplyDate, Cost, Quantity) 
    VALUES (@i, @SupplierID, '2024-05-01', @Cost, @Quantity);

    -- Zwiêkszanie licznika pêtli
